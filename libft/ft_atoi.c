#include "libft.h"

int		ft_atoi(const char *str)
{
	int		result;
	int		i;
	int		neg;

	result = 0;
	i = 0;
	neg = 1;
	while (str[i] && ((str[i] > 8 && str[i] < 14) || str[i] == 32))
		i++;
	if (str[i] == '-')
		neg = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - 48;
		i++;
	}
	return (result * neg);
}
